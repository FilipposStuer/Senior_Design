_STATE_COLORS = {
    State.IDLE:       (0, 200,   0),
    State.CONFIRMING: (0, 220, 255),
    State.MOVING:     (0, 165, 255),
    State.GRIPPING:   (0, 100, 255),
    State.DEPOSITING: (0,  60, 255),
}

def draw_hud(frame, state, fps, confirm_count, target, stats, homography_ok):
    h, w = frame.shape[:2]
    color = _STATE_COLORS.get(state, (255, 255, 255))

    cv2.rectangle(frame, (0, 0), (w, 46), (20, 20, 20), -1)
    cv2.putText(frame, f"State: {state.name}",
                (10, 30), cv2.FONT_HERSHEY_SIMPLEX, 0.7, color, 2)
    cv2.putText(frame, f"FPS {fps:.1f}",
                (w - 88, 30), cv2.FONT_HERSHEY_SIMPLEX, 0.55, (200, 200, 200), 1)

    if not homography_ok:
        cv2.putText(frame, "! NO HOMOGRAPHY — run calibrate_homography.py",
                    (10, 62), cv2.FONT_HERSHEY_SIMPLEX, 0.45, (0, 60, 255), 1)

    if target and state in (State.IDLE, State.CONFIRMING):
        info = (f"{target.class_name.upper()}  conf={target.confidence:.2f}"
                f"  ({target.x_cm:.1f},{target.y_cm:.1f}) cm"
                f"  bin={target.bin_name}")
        cv2.putText(frame, info, (10, 68),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.45, (0, 255, 120), 1)

    if state == State.CONFIRMING:
        ratio = min(confirm_count / CONFIRM_FRAMES_NEEDED, 1.0)
        bar_w = int((w - 20) * ratio)
        cv2.rectangle(frame, (10, 76), (w - 10, 86), (50, 50, 50), -1)
        cv2.rectangle(frame, (10, 76), (10 + bar_w, 86), (0, 220, 255), -1)
        cv2.putText(frame, f"Confirming {confirm_count}/{CONFIRM_FRAMES_NEEDED}",
                    (10, 100), cv2.FONT_HERSHEY_SIMPLEX, 0.44, (0, 220, 255), 1)

    px = w - 178
    cv2.rectangle(frame, (px, 50), (w, 140), (20, 20, 20), -1)
    cv2.putText(frame, f"Picks: {stats.picks_attempted}",
                (px + 6, 68),  cv2.FONT_HERSHEY_SIMPLEX, 0.42, (200, 200, 200), 1)
    cv2.putText(frame, f"OK:    {stats.picks_succeeded}",
                (px + 6, 84),  cv2.FONT_HERSHEY_SIMPLEX, 0.42, (0, 200, 80), 1)
    cv2.putText(frame, f"Fail:  {stats.picks_failed}",
                (px + 6, 100), cv2.FONT_HERSHEY_SIMPLEX, 0.42, (80, 80, 255), 1)
    y = 116
    for name, n in list(stats.counts.items())[:2]:
        cv2.putText(frame, f"{name}: {n}",
                    (px + 6, y), cv2.FONT_HERSHEY_SIMPLEX, 0.38, (160, 160, 160), 1)
        y += 14

    cv2.rectangle(frame, (0, h - 28), (w, h), (20, 20, 20), -1)
    cv2.putText(frame, "[H] home  [G] grip  [R] release  [Q] quit",
                (10, h - 10), cv2.FONT_HERSHEY_SIMPLEX, 0.42, (140, 140, 140), 1)


# ══════════════════════════════════════════════════════════════════
#  MAIN
# ══════════════════════════════════════════════════════════════════

def main():
    logger.info("=" * 60)
    logger.info("  Waste Sorter  -  6-DOF Robot Arm Vision Controller")
    logger.info("=" * 60)

    H = load_homography(HOMOGRAPHY_FILE)
    homography_ok = H is not None

    logger.info("Loading YOLO model: %s", MODEL_PATH)
    try:
        model = YOLO(MODEL_PATH)
        logger.info("Model ready. Classes: %s", model.names)
    except Exception as exc:
        logger.critical("Failed to load model: %s", exc)
        return

    logger.info("Opening camera index=%d", CAMERA_INDEX)
    camera = cv2.VideoCapture(CAMERA_INDEX)
    camera.set(cv2.CAP_PROP_FRAME_WIDTH,  FRAME_WIDTH)
    camera.set(cv2.CAP_PROP_FRAME_HEIGHT, FRAME_HEIGHT)
    camera.set(cv2.CAP_PROP_BUFFERSIZE, 1)

    if not camera.isOpened():
        logger.critical("Camera not found. Change CAMERA_INDEX.")
        return
    logger.info("Camera ready: %dx%d", FRAME_WIDTH, FRAME_HEIGHT)

    camera_lock = threading.Lock()

    arm = ArmComm(SERIAL_PORT, SERIAL_BAUDRATE, SERIAL_TIMEOUT)
    if not arm.connected:
        logger.warning("Arm not connected. Commands will be logged only.")

    state         = State.IDLE
    target        = None
    confirm_count = 0
    confirm_miss  = 0
    stats         = Stats()

    stop_event    = threading.Event()
    reached_event = threading.Event()
    fb_thread     = None

    frame_count = 0
    fps_time    = time.time()
    fps         = 0.0

    logger.info("System ready. Auto-pick ON (%d confirm frames).", CONFIRM_FRAMES_NEEDED)
    logger.info("Controls: [H] home  [G] grip  [R] release  [Q] quit")
    logger.info("-" * 60)

    try:
        while True:

            with camera_lock:
                ret, frame = camera.read()
            if not ret:
                logger.error("Camera read failed — exiting")
                break

            frame_count += 1
            if frame_count % 30 == 0:
                now      = time.time()
                fps      = 30.0 / max(now - fps_time, 1e-6)
                fps_time = now

            # ── MOVING: feedback loop sent GRIP ──────────────────
            if state == State.MOVING and reached_event.is_set():
                state = State.GRIPPING
                stop_event.set()
                if fb_thread:
                    fb_thread.join(timeout=2.0)
                fb_thread = None
                stats.picks_attempted += 1
                logger.info("Object reached. Waiting for grip + lift...")

                grip_done = arm.wait_done(timeout_override=20)

                if grip_done:
                    state  = State.DEPOSITING
                    dep_ok = deposit_sequence(arm, target, stats)
                    if not dep_ok:
                        stats.picks_failed += 1
                else:
                    logger.error("Grip sequence failed.")
                    stats.picks_failed += 1
                    arm.release()
                    arm.home()
                    arm.wait_done(timeout_override=15)

                state         = State.IDLE
                target        = None
                confirm_count = 0
                confirm_miss  = 0
                stop_event.clear()
                reached_event.clear()

            # ── IDLE / CONFIRMING: run detection ─────────────────
            if state in (State.IDLE, State.CONFIRMING):
                best = detect_best(frame, model, H)

                if best:
                    if state == State.IDLE:
                        state = State.CONFIRMING

                    confirm_count += 1
                    confirm_miss   = 0
                    target         = best

                    # Auto-pick trigger
                    if confirm_count >= CONFIRM_FRAMES_NEEDED:
                        logger.info(
                            "Auto-pick: %s  conf=%.2f  (%.2f, %.2f) cm  bbox=(%d,%d)",
                            target.class_name, target.confidence,
                            target.x_cm, target.y_cm,
                            target.bbox_w, target.bbox_h,
                        )

                        sent = arm.pick(
                            target.class_name,
                            target.x_cm, target.y_cm,
                            target.bbox_w, target.bbox_h,
                        )

                        if sent:
                            state         = State.MOVING
                            confirm_count = 0

                            # Wait for arm to reach position before
                            # starting the feedback monitor
                            logger.info("Waiting for arm to reach pick position...")
                            arm_ok = arm.wait_done(timeout_override=20)

                            if arm_ok:
                                stop_event.clear()
                                reached_event.clear()
                                fb_thread = threading.Thread(
                                    target=run_feedback_loop,
                                    args=(
                                        camera, camera_lock, model, arm,
                                        target.class_id,
                                        stop_event, reached_event,
                                    ),
                                    daemon=True,
                                )
                                fb_thread.start()
                                logger.info("Arm in position. Feedback loop watching for grip trigger.")
                            else:
                                logger.error("Arm did not reach position — aborting.")
                                state         = State.IDLE
                                confirm_count = 0
                        else:
                            logger.error("PICK send failed — aborting.")
                            state         = State.IDLE
                            confirm_count = 0

                else:
                    # No detection this frame
                    confirm_miss += 1
                    if confirm_miss >= CONFIRM_MISS_ALLOWED:
                        if state == State.CONFIRMING:
                            logger.debug("Confirmation lost — back to IDLE")
                        state         = State.IDLE
                        confirm_count = 0
                        confirm_miss  = 0
                        target        = None

            # ── HUD + display ────────────────────────────────────
            draw_hud(frame, state, fps, confirm_count, target, stats, homography_ok)
            cv2.imshow("Waste Sorter - 6DOF Robot Vision", frame)

            key = cv2.waitKey(1) & 0xFF
            if key == ord("q"):
                logger.info("Quit requested.")
                break
            elif key == ord("h") and state == State.IDLE:
                arm.home()
            elif key == ord("g") and state == State.IDLE:
                arm.grip()
            elif key == ord("r") and state == State.IDLE:
                arm.release()

    except KeyboardInterrupt:
        logger.info("Interrupted by user (Ctrl+C).")

    finally:
        logger.info("Shutting down...")
        stop_event.set()
        if fb_thread and fb_thread.is_alive():
            fb_thread.join(timeout=2.0)
        arm.release()
        arm.home()
        camera.release()
        cv2.destroyAllWindows()
        arm.close()
        logger.info(
            "Final stats -> attempted=%d  OK=%d  fail=%d  by_class=%s",
            stats.picks_attempted, stats.picks_succeeded,
            stats.picks_failed, dict(stats.counts),
        )
        logger.info("Shutdown complete.")


if __name__ == "__main__":
    main()
