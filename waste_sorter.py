"""
waste_sorter.py  -  Raspberry Pi Vision Controller
====================================================
6-DOF Robot Arm  |  Waste Classification & Sorting
----------------------------------------------------

FOLDER STRUCTURE (all in the same folder):
    waste_sorter.py
    best.pt
    homography.npy        <- generated by calibrate_homography.py

FIRMWARE PROTOCOL (Demo_march_24_fixed.ino):
    Pi sends:
        PICK <class> <x_cm> <y_cm> <bbox_w> <bbox_h>
        GRIP
        RELEASE
        HOME

    ItsyBitsy replies:
        DONE
        ERROR

WORKSPACE LIMITS (from ik_table.h):
    x = 8.5 .. 13.5 cm
    y = 0.6 ..  4.4 cm

STATE MACHINE:
    IDLE -> CONFIRMING -> MOVING -> GRIPPING -> DEPOSITING -> IDLE

HOW TO RUN:
    1. Run calibrate_homography.py once to generate homography.npy
    2. Adjust BIN_ANGLES to match your physical bin positions
    3. python waste_sorter.py
"""

