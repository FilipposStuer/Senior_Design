class State(Enum):
    IDLE        = auto()
    CONFIRMING  = auto()
    MOVING      = auto()
    GRIPPING    = auto()
    DEPOSITING  = auto()


# ══════════════════════════════════════════════════════════════════
#  DATA CLASSES
# ══════════════════════════════════════════════════════════════════

@dataclass
class Detection:
    class_id:   int
    class_name: str
    bin_name:   str
    confidence: float
    cx:         int
    cy:         int
    bbox_w:     int
    bbox_h:     int
    x_cm:       float
    y_cm:       float

@dataclass
class Stats:
    picks_attempted: int = 0
    picks_succeeded: int = 0
    picks_failed:    int = 0
    counts: dict = field(default_factory=lambda: collections.defaultdict(int))


# ══════════════════════════════════════════════════════════════════
#  HOMOGRAPHY / COORDINATE CONVERSION
# ══════════════════════════════════════════════════════════════════

def load_homography(path: str) -> np.ndarray | None:
    if not os.path.exists(path):
        logger.warning("Homography file '%s' not found.", path)
        logger.warning("Run calibrate_homography.py first for accurate coordinates.")
        return None
    H = np.load(path)
    logger.info("Homography loaded from '%s'  shape=%s", path, H.shape)
    return H


def pixel_to_cm(cx: int, cy: int, H: np.ndarray | None) -> tuple[float, float]:
    if H is not None:
        pt     = np.array([[[float(cx), float(cy)]]], dtype=np.float32)
        result = cv2.perspectiveTransform(pt, H)
        return round(float(result[0][0][0]), 2), round(float(result[0][0][1]), 2)
    x_cm = 11.0 + (cx - FRAME_WIDTH  / 2.0) * 0.034
    y_cm =  2.5 + (cy - FRAME_HEIGHT / 2.0) * 0.034
    return round(x_cm, 2), round(y_cm, 2)


# ══════════════════════════════════════════════════════════════════
#  DETECTION  (Fixed_vision engine — stream=True)
# ══════════════════════════════════════════════════════════════════

def draw_box(img, xyxy, label: str, color=(0, 255, 0)):
    x1, y1, x2, y2 = map(int, xyxy)
    cv2.rectangle(img, (x1, y1), (x2, y2), color, 2)
    cv2.putText(img, label, (x1, max(0, y1 - 8)),
                cv2.FONT_HERSHEY_SIMPLEX, 0.55, color, 2)


def detect_best(frame: np.ndarray, model: YOLO,
                H: np.ndarray | None) -> Detection | None:
    """
    Run YOLO on one frame using stream=True.
    Returns the highest-confidence detection regardless of workspace range.
    Draws boxes on the frame in-place.
    """
    results = model.predict(
        frame,
        conf=CONFIDENCE_THRESHOLD,
        iou=NMS_IOU,
        imgsz=INFER_SIZE,
        verbose=False,
        stream=True,
    )
    r = next(results, None)

    if r is None or r.boxes is None or len(r.boxes) == 0:
        return None

    xyxys = r.boxes.xyxy.cpu().numpy()
    confs = r.boxes.conf.cpu().numpy()
    clss  = r.boxes.cls.cpu().numpy().astype(int)

    best: Detection | None = None
    best_conf = 0.0

    for xyxy, conf, c in zip(xyxys, confs, clss):
        class_id   = int(c)
        final_conf = float(conf)

        info = WASTE_CLASSES.get(class_id)
        if info is None:
            continue

        class_name = info["name"]
        bin_name   = info["bin"]
        color      = CLASS_COLORS.get(class_name, (0, 255, 0))

        x1, y1, x2, y2 = map(int, xyxy)
        cx     = (x1 + x2) // 2
        cy     = (y1 + y2) // 2
        bbox_w = x2 - x1
        bbox_h = y2 - y1
        x_cm, y_cm = pixel_to_cm(cx, cy, H)

        # Draw box and labels
        draw_box(frame, xyxy, f"{class_name} {final_conf:.2f}", color)
        cv2.circle(frame, (cx, cy), 5, (0, 0, 255), -1)
        cv2.putText(frame, f"({x_cm:.1f},{y_cm:.1f}) cm",
                    (x1, y2 + 16), cv2.FONT_HERSHEY_SIMPLEX,
                    0.38, (0, 210, 255), 1)
        cv2.putText(frame, f"bin: {bin_name}",
                    (x1, y2 + 30), cv2.FONT_HERSHEY_SIMPLEX,
                    0.36, (180, 180, 180), 1)

        if final_conf > best_conf:
            best_conf = final_conf
            best = Detection(
                class_id=class_id, class_name=class_name, bin_name=bin_name,
                confidence=final_conf, cx=cx, cy=cy,
                bbox_w=bbox_w, bbox_h=bbox_h,
                x_cm=x_cm, y_cm=y_cm,
            )

    return best


# ══════════════════════════════════════════════════════════════════
#  SERIAL COMMUNICATION
# ══════════════════════════════════════════════════════════════════

class ArmComm:
    """Thread-safe serial interface to Demo_march_24_fixed.ino."""

    def __init__(self, port: str, baudrate: int, timeout: int):
        self.port      = port
        self.baudrate  = baudrate
        self.timeout   = timeout
        self.conn      = None
        self.connected = False
        self._lock     = threading.Lock()
        self._connect()

    def _connect(self):
        try:
            self.conn = serial.Serial(
                port=self.port, baudrate=self.baudrate,
                timeout=1, write_timeout=1,
            )
            time.sleep(2)
            self.conn.reset_input_buffer()
            self.connected = True
            logger.info("ArmComm: connected on %s @ %d baud", self.port, self.baudrate)
        except serial.SerialException as exc:
            logger.error("ArmComm: connection failed: %s", exc)
            self.connected = False

    def send(self, command: str) -> bool:
        if not self.connected or not self.conn or not self.conn.is_open:
            logger.warning("ArmComm: not connected — dropped: %s", command.strip())
            return False
        payload = command.strip() + "\n"
        try:
            with self._lock:
                self.conn.write(payload.encode())
            logger.info("TX >> %s", payload.strip())
            return True
        except serial.SerialException as exc:
            logger.error("ArmComm: write error: %s", exc)
            return False

    def pick(self, class_name: str, x_cm: float, y_cm: float,
             bbox_w: int, bbox_h: int) -> bool:
        return self.send(
            f"PICK {class_name.upper()} {x_cm:.2f} {y_cm:.2f} {bbox_w} {bbox_h}"
        )

    def move(self, x_cm: float, y_cm: float) -> bool:
        return self.send(f"MOVE {x_cm:.2f} {y_cm:.2f}")

    def grip(self) -> bool:
        return self.send("GRIP")

    def release(self) -> bool:
        return self.send("RELEASE")

    def home(self) -> bool:
        return self.send("HOME")

    def wait_done(self, timeout_override: int | None = None) -> bool:
        deadline = time.time() + (timeout_override or self.timeout)
        while time.time() < deadline:
            try:
                if self.conn and self.conn.in_waiting:
                    line = (self.conn.readline()
                            .decode(errors="replace").strip().upper())
                    logger.info("RX << %s", line)
                    if "DONE"  in line: return True
                    if "ERROR" in line:
                        logger.error("ArmComm: firmware ERROR")
                        return False
                time.sleep(0.05)
            except serial.SerialException as exc:
                logger.error("ArmComm: read error: %s", exc)
                return False
        logger.error("ArmComm: timed out waiting for DONE")
        return False

    def close(self):
        if self.conn and self.conn.is_open:
            self.conn.close()
            logger.info("ArmComm: connection closed")


# ══════════════════════════════════════════════════════════════════
#  FEEDBACK LOOP  (monitor only — no movement commands)
# ══════════════════════════════════════════════════════════════════

def run_feedback_loop(
    camera:          cv2.VideoCapture,
    camera_lock:     threading.Lock,
    model:           YOLO,
    arm:             ArmComm,
    target_class_id: int,
    stop_event:      threading.Event,
    reached_event:   threading.Event,
):
    """
    Runs AFTER the arm has reached the pick position (wait_done returned).
    Does NOT send movement commands — arm is already in position.
    Only watches for the object to disappear, then sends GRIP.
    """
    disappear_count = 0
    logger.info("Feedback loop started — monitoring class_id %d", target_class_id)

    while not stop_event.is_set():
        with camera_lock:
            ret, frame = camera.read()

        if not ret:
            logger.warning("Feedback loop: failed to read frame.")
            time.sleep(0.1)
            continue

        results = model.predict(
            frame,
            conf=CONFIDENCE_THRESHOLD,
            iou=NMS_IOU,
            imgsz=INFER_SIZE,
            verbose=False,
            stream=True,
        )
        r = next(results, None)

        object_visible = False
        if r is not None and r.boxes is not None and len(r.boxes) > 0:
            for conf, c in zip(
                r.boxes.conf.cpu().numpy(),
                r.boxes.cls.cpu().numpy().astype(int),
            ):
                if int(c) == target_class_id and float(conf) >= CONFIDENCE_THRESHOLD:
                    object_visible = True
                    break

        if object_visible:
            disappear_count = 0
            logger.debug("Feedback: object still visible")
        else:
            disappear_count += 1
            logger.debug("Feedback: object missing (%d/%d)",
                         disappear_count, DISAPPEAR_THRESHOLD)
            if disappear_count >= DISAPPEAR_THRESHOLD:
                logger.info("Feedback: object gone — sending GRIP")
                arm.grip()
                reached_event.set()
                break

        time.sleep(0.1)

    logger.info("Feedback loop stopped.")


# ══════════════════════════════════════════════════════════════════
#  DEPOSIT SEQUENCE
# ══════════════════════════════════════════════════════════════════

def deposit_sequence(arm: ArmComm, target: Detection, stats: Stats) -> bool:
    """Move to deposit position, release, return home."""
    logger.info("Depositing '%s'", target.class_name)

    X_DEPOSIT = 11.0   # cm — tune for your setup
    Y_DEPOSIT  =  2.0  # cm — tune for your setup

    ok = arm.move(X_DEPOSIT, Y_DEPOSIT)
    if ok:
        ok = arm.wait_done(timeout_override=15)

    if ok:
        arm.release()
        time.sleep(0.5)
        stats.picks_succeeded += 1
        stats.counts[target.class_name] += 1
        logger.info("Deposit OK. Counts: %s", dict(stats.counts))
    else:
        logger.error("Deposit move failed — releasing in place.")
        arm.release()
        stats.picks_failed += 1

    arm.home()
    arm.wait_done(timeout_override=15)
    return ok


