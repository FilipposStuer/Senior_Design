MODEL_PATH        = "best.pt"
HOMOGRAPHY_FILE   = "homography.npy"

CONFIDENCE_THRESHOLD = 0.25
NMS_IOU              = 0.45
INFER_SIZE           = 320

CAMERA_INDEX  = 0        # 0 = built-in | 1 = USB Logitech
FRAME_WIDTH   = 640
FRAME_HEIGHT  = 480

SERIAL_PORT     = "COM5"
SERIAL_BAUDRATE = 9600
SERIAL_TIMEOUT  = 40

CONFIRM_FRAMES_NEEDED = 3
CONFIRM_MISS_ALLOWED  = 4
DISAPPEAR_THRESHOLD   = 5

WS_X_MIN, WS_X_MAX = 8.5, 13.5
WS_Y_MIN, WS_Y_MAX = 0.6,  4.4

BIN_ANGLES = {
    "plastic":   60,
    "paper":     90,
    "cardboard": 120,
    "general":   90,
}

WASTE_CLASSES = {
    0: {"name": "general",   "bin": "landfill"},
    1: {"name": "paper",     "bin": "paper"},
    2: {"name": "plastic",   "bin": "plastic"},
    3: {"name": "cardboard", "bin": "cardboard"},
}

CLASS_COLORS = {
    "general":   (128, 128, 128),
    "paper":     (200, 200, 255),
    "plastic":   (255,  80,  80),
    "cardboard": ( 50, 165, 255),
}


